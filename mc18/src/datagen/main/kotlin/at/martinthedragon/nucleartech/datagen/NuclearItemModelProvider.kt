    private val cubeAll = getExistingFile(mcLoc("block/cube_all"))
    private val generatedItem = getExistingFile(mcLoc("item/generated"))
    private val spawnEggItem = getExistingFile(mcLoc("item/template_spawn_egg"))
    private lateinit var bombKitItem: ModelFile

    private fun simpleItem(item: Item) {
        getBuilder(item.registryName!!.path)
            .parent(generatedItem)
            .texture("layer0", itemTexture(item))
    }

    private fun bombKit(item: Item) {
        getBuilder(item.registryName!!.path).parent(bombKitItem)
    }

    private fun spawnEgg(item: Item) {
        getBuilder(item.registryName!!.path).parent(spawnEggItem)
    }

    private fun bucket(fluid: Fluid, bucket: BucketItem, flipGas: Boolean = fluid.attributes.isLighterThanAir) {
        withExistingParent(bucket.registryName!!.path, ResourceLocation("forge", "item/bucket"))
            .customLoader { modelBuilder: ItemModelBuilder, existingFileHelper -> DynamicBucketModelBuilder.begin(modelBuilder, existingFileHelper) }
            .fluid(fluid)
            .flipGas(flipGas)
            .end()
    }

    private fun specialEntityItem(item: Item) = getBuilder(item.registryName!!.path).parent(specialEntityModel)

    private fun blockTexture(block: Block) =
        ResourceLocation(block.registryName!!.namespace, "$BLOCK_FOLDER/${block.registryName!!.path}")

    private fun itemTexture(item: Item) =
        ResourceLocation(item.registryName!!.namespace, "$ITEM_FOLDER/${item.registryName!!.path}")
